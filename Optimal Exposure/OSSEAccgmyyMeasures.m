function  [yp,yn] = OSSEAccgmyyMeasures(par,alpha)
cp=abs(par(1));
bp=abs(par(2));
yp=abs(par(3));
cn=abs(par(4));
bn=abs(par(5));
yn=abs(par(6));
parvec=[cp cn 1./bn 1./bp yp yn];
var=ccgmyyqvar(parvec);
[xv,~]=NUG(1,2,.5,var,200);
%xxxx=nonzeros(-1:.004:1);
xxxx=nonzeros(xv);
dxa=diff(xxxx);
dx=[dxa' dxa(end)]';
M=1./bp;
G=1./bn;
xxxxn=nonzeros(xxxx.*(xxxx<0));
xxxxp=nonzeros(xxxx.*(xxxx>0));
dxn=nonzeros(dx.*(xxxx<0));
dxp=nonzeros(dx.*(xxxx>0));
yyap=cp.*abs(xxxxp).^(2*alpha-yp-1).*exp(-M.*abs(xxxxp)).*dxp;%.004;
yyan=cn.*abs(xxxxn).^(2*alpha-yn-1).*exp(-G.*abs(xxxxn)).*dxn;%.004;
yy1p=cp.*abs(xxxxp).^(2-yp-1).*exp(-M.*abs(xxxxp)).*dxp;%.004;
yy1n=cn.*abs(xxxxn).^(2-yn-1).*exp(-G.*abs(xxxxn)).*dxn;%.004;
yy2p=cp.*abs(xxxxp).^(4-yp-1).*exp(-M.*abs(xxxxp)).*dxp;%.004;
yy2n=cn.*abs(xxxxn).^(4-yn-1).*exp(-G.*abs(xxxxn)).*dxn;%.004;
yy0p=cp.*abs(xxxxp).^(-yp-1).*exp(-M.*abs(xxxxp)).*dxp;%.004;
yy0n=cn.*abs(xxxxn).^(-yn-1).*exp(-G.*abs(xxxxn)).*dxn;%.004;
outpm=[xxxxp yyap yy1p yy2p yy0p];
outnm=[xxxxn yyan yy1n yy2n yy0n];
yp=outpm;
yn=outnm;
end
